# @param {Character[][]} board
# @param {String} word
# @return {Boolean}
def exist(board, word)
  @rows = board.size
  @cols = board[0].size

  0.upto(@rows - 1).each do |i|
    0.upto(@cols - 1).each do |j|
      if board[i][j] == word[0]
        @visited = Array.new(@rows) { Array.new(@cols, false) }
        return true if dfs(i, j, board, word, 0)
      end
    end
  end
  false
end

def dfs(i, j, board, word, idx)
  return false if i < 0 || j < 0 || i > @rows - 1 || j > @cols - 1 || board[i][j] != word[idx] || @visited[i][j]

  return true if idx == word.size - 1

  @visited[i][j] = true

  found = dfs(i, j + 1, board, word, idx + 1) || # right
          dfs(i + 1, j, board, word, idx + 1) || # down
          dfs(i, j - 1, board, word, idx + 1) || # left
          dfs(i - 1, j, board, word, idx + 1) || # up

  @visited[i][j] = false
  found
end

